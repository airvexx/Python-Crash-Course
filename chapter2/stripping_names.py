## Removes whitespace and prints a name

first_name = " Hagbard"
last_name = " Celine "
full_name =  first_name + " " + last_name

print(full_name)

first_name = " Hagbard"
first_name = first_name.lstrip()
last_name = " Celine "
last_name = last_name.strip()
full_name =  first_name + " " + last_name

print(full_name)
